function f(name: string, type: CliFlag['type'], summary: string, extra: Partial<CliFlag> = {}): CliFlag {
  return { name, type, summary, ...extra };
}

function av(id: string, primary: string, secondary: string | undefined, usage: string, summary: string, flags: CliFlag[], example: string, ref: string): CliManifestVerb {
  return { id, primaryVerb: primary, secondaryVerb: secondary, usage, summary, flags, canonical_example: example, source_ref: ref, status: 'available' };
}

function pl(id: string, primary: string, secondary: string | undefined, usage: string, summary: string, ref: string): CliManifestVerb {
  return { id, primaryVerb: primary, secondaryVerb: secondary, usage, summary, flags: [], canonical_example: `ant ${usage} # planned — design open`, source_ref: ref, repo: 'delivery-plan', status: 'planned' };
}

function nw(id: string, primary: string, secondary: string | undefined, usage: string, summary: string, flags: CliFlag[], example: string, ref: string): CliManifestVerb {
  return { id, primaryVerb: primary, secondaryVerb: secondary, usage, summary, flags, canonical_example: example, source_ref: ref, status: 'needs-wrapper' };
}

