/**
 * CLI manifest — single source of truth for every `ant` verb.
 *
 * The Fresh-ANT manual rule says every customer-facing promise has a visible
 * CLI path. Discovery docs, deck CLI companion slides, and
 * `ant docs generate --from-cli` all read from this module so the three
 * surfaces stay in agreement.
 *
 * Each verb declares: usage signature, plain summary, flag list, one
 * canonical example, a grep-able source_ref, and a status.
 *
 * Status meanings:
 *   - 'available'     implemented today; grep source_ref to verify.
 *   - 'needs-wrapper' capability exists server-side, CLI shape sketched in
 *                     DELIVERY-PLAN.md but no wrapper module yet.
 *   - 'planned'       named in DELIVERY-PLAN.md or the fresh-ANT manual,
 *                     CLI wrapper not yet shipped. Provenance is recorded
 *                     in source_ref (DP path vs MANUAL path).
 */

export type CliVerbStatus = 'available' | 'needs-wrapper' | 'planned';

export type CliFlag = {
  name: string;
  type: 'string' | 'number' | 'boolean' | 'enum';
  default?: string;
  constraint?: string;
  summary: string;
};

export type CliVerbRepo = 'fresh-ant' | 'v3' | 'delivery-plan';

export type CliManifestVerb = {
  id: string;
  primaryVerb: string;
  secondaryVerb?: string;
  usage: string;
  summary: string;
  flags: CliFlag[];
  canonical_example: string;
  source_ref: string;
  /** Which repo source_ref resolves against. fresh-ant → /CascadeProjects/ant, v3 → /CascadeProjects/a-nice-terminal, delivery-plan → ANT-Open-Slide spec. Optional; defaults to 'fresh-ant'. */
  repo?: CliVerbRepo;
  status: CliVerbStatus;
  since_version?: string;
};

